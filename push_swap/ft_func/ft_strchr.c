#include "../push_swap.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	if (!c || !s)
		return (0);
	while (s[i])
	{
		if (s[i] == (unsigned char)c)
			return (&((char *)s)[i]);
		i++;
	}
	if (s[i] == (unsigned char)c)
		return (&((char *)s)[i]);
	return (0);
}
